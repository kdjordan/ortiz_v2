<template>
	<section class="collage">
		<h2 class="collage__type" aria-hidden="true">Ortiz<br />Metals</h2>

		<div class="collage__grid">
			<figure ref="featureEl" class="feature">
				<picture>
					<source type="image/avif" :srcset="srcset(feature.base, 'avif')" :sizes="featSizes" />
					<source type="image/webp" :srcset="srcset(feature.base, 'webp')" :sizes="featSizes" />
