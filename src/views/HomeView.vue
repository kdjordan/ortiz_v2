<script setup>
	import { computed, nextTick, onMounted, ref } from 'vue';
	import { useHead } from '@unhead/vue';
	import gsap from 'gsap';
	import gallery from '@/gallery.json';

	// Flatten each work's caption onto the work so the template keeps using
	// img.base / img.holder / img.desc / img.year (index = stable render order).
	const images = gallery.works.map((work) => ({
		index: work.order,
		base: work.base,
		holder: work.caption.holder,
		desc: work.caption.desc,
		year: work.caption.year,
	}));

	useHead({
		title: 'Ortiz Metals — Custom Sculpture & Metalwork | Randy Ortiz, Eugene OR',
		meta: [
			{
				name: 'description',
				content:
					'Custom sculpture, architectural metalwork, and site-specific public art by Randy Ortiz, a Eugene, Oregon based artist.',
			},
		],
	});

	const featureIndex = ref(0);
	const featureImg = ref(null);
	const featureEl = ref(null);

	const feature = computed(() => images[featureIndex.value] || {});
	const tiles = computed(() =>
		images.map((img, i) => ({ img, i })).filter((t) => t.i !== featureIndex.value)
	);
	const pad = (n) => String(n).padStart(2, '0');

	// responsive image helpers (variants generated by scripts/optimize-images.mjs)
	const srcset = (base, ext) => `${base}-450.${ext} 450w, ${base}-900.${ext} 900w`;
	const featSizes = '(max-width: 820px) 92vw, 56vw';
	const tileSizes = '(max-width: 820px) 46vw, 22vw';

	// slow, living "breath" on the hero so it never feels static
	let drift = null;
	function startDrift() {
		if (drift) drift.kill();
		if (!featureImg.value) return;
		if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) return;
		drift = gsap.fromTo(
			featureImg.value,
			{ scale: 1 },
			{ scale: 1.06, duration: 9, ease: 'sine.inOut', yoyo: true, repeat: -1 }
		);
	}

	function select(i) {
		if (i === featureIndex.value) return;
		featureIndex.value = i;
		nextTick(() => {
			if (drift) drift.kill();
			if (featureImg.value) {
				gsap.fromTo(
					featureImg.value,
					{ clipPath: 'inset(0 0 0 100%)', scale: 1.12 },
					{
						clipPath: 'inset(0 0 0 0%)',
						scale: 1,
						duration: 0.8,
						ease: 'power4.out',
						onComplete: startDrift,
					}
				);
			}
			// On mobile the feature sits above the tiles — bring the swapped work into view.
			if (window.matchMedia('(max-width: 820px)').matches && featureEl.value) {
				featureEl.value.scrollIntoView({ behavior: 'smooth', block: 'start' });
			}
		});
	}

	onMounted(() => {
		const tl = gsap.timeline({ delay: 0.15 });

		tl.fromTo(
			'.collage__type',
			{ yPercent: 18, opacity: 0 },
			{ yPercent: 0, opacity: 0.07, duration: 1.2, ease: 'power4.out' },
			0
		)
			// hero wipes in from the right edge while the image settles out of an overscale
			.fromTo(
				'.feature',
				{ clipPath: 'inset(0 0 0 100%)' },
				{ clipPath: 'inset(0 0 0 0%)', duration: 1, ease: 'power4.out' },
				0.1
			)
			.fromTo(
				'.feature img',
				{ scale: 1.2 },
				{ scale: 1, duration: 1.4, ease: 'power3.out', onComplete: startDrift },
				0.1
			)
			// tiles cascade in after the hero is established
			.fromTo(
				'.collage .tile',
				{ y: 40, opacity: 0, clipPath: 'inset(0 0 100% 0)' },
				{
					y: 0,
					opacity: 1,
					clipPath: 'inset(0 0 0% 0)',
					duration: 0.85,
					stagger: 0.08,
					ease: 'power3.out',
				},
				0.5
			);
	});
</script>

